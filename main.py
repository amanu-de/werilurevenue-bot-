def save_user(uid):
    if not os.path.exists(USER_LIST_FILE):
        with open(USER_LIST_FILE, "w") as f: f.write("")
    with open(USER_LIST_FILE, "r") as f:
        users = f.read().splitlines()
    if str(uid) not in users:
        with open(USER_LIST_FILE, "a") as f:
            f.write(f"{uid}\n")

